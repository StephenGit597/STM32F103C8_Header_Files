//Interrupt Priority Register
#define STM32F103C8_Interrupt_CHx_MaX_Num 42
#define STM32F103C8_Config_num_MaX_Num 15
static inline void Interrupt_Priority_Config(unsigned char Interrupt_CHx,unsigned char Config_num)
{
	if((Interrupt_CHx>STM32F103C8_Interrupt_CHx_MaX_Num)||(Config_num>STM32F103C8_Config_num_MaX_Num))
	{
		return;
	}
	unsigned char base = Interrupt_CHx/4;
	unsigned char offset = Interrupt_CHx%4;
	NVIC->IPR[base] = NVIC->IPR[base]&~(0x0000000F<<(offset*8+4));
	NVIC->IPR[base] = NVIC->IPR[base]|(Config_num<<(offset*8+4));
}

